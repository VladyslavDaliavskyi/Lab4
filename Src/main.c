/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_CAN1_Init();
  /* USER CODE BEGIN 2 */
	initLCD();
	
	CAN_TxHeaderTypeDef tx;
	tx.DLC = 8;
	tx.ExtId = 0x122;
	tx.StdId = 0x02;
	tx.RTR = CAN_RTR_DATA;
	tx.IDE = CAN_ID_STD;
	tx.TransmitGlobalTime = DISABLE;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
		switch(functionNumber){
			case 0:
				clear();
				lcdUNumber(HAL_CAN_GetRxFifoFillLevel(&hcan1,CAN_RX_FIFO0));
				break;
			case 1: //SendOneMoreMessagePack
				clear();
				HAL_CAN_AddTxMessage(&hcan1,&tx,(uint8_t*)"abcdefe",&TxMailbox);
				lcdString("Pack sended");
				functionNumber = 0;
				break;
			case 2: //ReceiveOneMessagePack if exist
				if(HAL_CAN_GetRxFifoFillLevel(&hcan1,CAN_RX_FIFO0)>0) {
					clear();
					HAL_CAN_GetRxMessage(&hcan1,CAN_RX_FIFO0,&rx,RXdata);
					lcdString("Pack received");
				} else {
					clear();
					lcdString("No packs");
					setCursor(0,1);
					lcdString("to receive!");
				}
				functionNumber = 0;
				break;
		}	
		HAL_Delay(1000);
    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

